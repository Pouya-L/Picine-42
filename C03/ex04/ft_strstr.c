#include <stdio.h>

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	i = 0;
	if (to_find[0] == '\0')
		return (str);
	while (str[i] != '\0')
	{
		j = 0;
		if (str[i] == to_find[j])
		{
			while (str[i + j] == to_find[j])
			{
				j++;
				if (to_find[j] == '\0')
					return (&str[i]);
			}
		}
		i++;
	}
	return (0);
}
int main(void)
{
    char str[50] = "hello my P name is Pouya and I want to break free";
    char to_final[] = "Pouya";
    printf ("%s\n", ft_strstr(str, to_final));
    return (0);
}
